package differentDataType;

public class Multiplication {
	public void multiplicationTwoNumbers(int num1, int num2) {

		int sum = num1 * num2;
		System.out.println(sum);
	}

	public void multiplicationThreeNumbers(int num1, int num2, int num3) {

		int sum = num1 * num2 * num3;
		System.out.println(sum);
	}
	
	public void multiplicationFourNumbers(int num1, int num2, int num3, int num4) {

		int sum = num1 * num2 * num3*num4;
		System.out.println(sum);
	}

}
